     * @return \Illuminate\Http\Response
     */
    public function index(Request $request)
    {
        $coursetypes=CourseType::find($request->input('coursetype'));
        $level=$request->input('level');
        $subjects=DB::table('subjects')
            ->select('subjects.title')
            ->where('subjects.coursetype_id','=',$request->input('coursetype'))
            ->where('subjects.level', '=',$level)
            ->get(); 
        $numberOfSubjects=$subjects->count();
        return view('showSubjects',compact('subjects','coursetypes','numberOfSubjects','level'));
    }

